package Chap1;

/**
 * (Area and perimeter of a circle) Write a program that displays the area and perimeter
 of a circle that has a radius of 5.5 using the following formula:
 perimeter = 2 * radius * PI
 area = radius * radius * PI
 */

public class Exercise_1_8_Area_Perimeter_Of_Circle {

    public static void main(String[] args) {
        System.out.print("Perimeter of circle with radius of 5.5");
        System.out.print(2 * 5.5 * 3.14);
        System.out.print("\nArea of circle with radius of 5.5");
        System.out.print(5.5 * 5.5 * 3.14);
    }
}
